#include <stdio.h>

int main()
{
    int a;
    a = 34;

    // float b = 1.4;
    float b;
    b = 23;

    // char c = 'f';
    char c;
    c = 'd';

    printf("The value of a is %d\n" , a);  //%d is used for integers
    printf("The value of b is %f\n" , b);  //%f is used for float
    printf("The value of c is %c\n" , c);  //%c is used for characters

    return 0;
}

