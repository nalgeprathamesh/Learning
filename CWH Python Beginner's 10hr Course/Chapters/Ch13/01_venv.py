# We can install a custom virtual enviroment to run specfic things
# For example if we use install pandas it normally it is installed globally
# But let's say we want to install pandas 1.5.3 only for this folder , then in
# that case we create a virtual enviroment

# We can create a new virtual enviroment by typing virtualenv {name}

# To activate the enviroment we can do something like this
# .\env\Scripts\activate.ps1
# entering this will activate the enviroment

