# Tuple is not only collection of values but also immutable
# a = (1) This is not a tuple. this will be considered integer
# a = (1,) This is a tuple, you need to add comma to prove that a single element is tuple
name = (12, 27823, True , "Prathamesh", 263.782367)
# This is a tuple and its value cannot be changed in any way possible
print(name)
print(type(name))